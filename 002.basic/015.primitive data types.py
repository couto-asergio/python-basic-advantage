# Primitive Data Types
"""
str - string - 'this' or "This"
int - integer - 123456 0 -10 -20 -50 -60 -15000 1500
float - real/floating point - 10.50 1.5 -10.10 -50.93 0.0
bool - boolean/logical  -  true/False  10 == 10
"""

print('Cris', type('Cris'))
print(10, type(10))
print(25.23, type('John'))
print(10 == 10, type('John'))
print('l' == 'L', type('l' == 'L'))
print('very', type('very'), bool('very'))

# String name
print('my name', type('my name'))
